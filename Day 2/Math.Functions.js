a = Math.E; console.log(a);  //Output : 2.718281828459045
a = Math.round(5.8); console.log(a);  //Output : 6
a = Math.floor(5.8); console.log(a);  //Output : 5
a = Math.ceil(5.7); console.log(a);  //Output : 6
a = Math.abs(-89); console.log(a);  //Output : 89
a = Math.trunc(12.444); console.log(a);  //Output : 12
a = Math.pow(2,4); console.log(a);  //Output : 16
a = Math.random(); console.log(a);  //Output : generate random value
